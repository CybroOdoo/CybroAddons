from odoo import fields, models


class PosConfig(models.Model):
    """Set the lock and time limit for the session lock configuration"""
    _inherit = 'pos.config'

    is_pos_lock = fields.Boolean(string='Enable Lock Screen', help='Lock '
                                                                   'Screen')
    idle_time_limit = fields.Float(string="Idle time limit", default=5,
                                   help='Time limit in minutes and seconds')
