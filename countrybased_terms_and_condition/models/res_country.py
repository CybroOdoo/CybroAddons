"""Module to customise the terms and condition depending on the country of
the partners country"""
from odoo import fields, models


class ResCountry(models.Model):
    """
    Inherits from Res. Country to add a field for terms and conditions.
    """
    _inherit = 'res.country'
    sale_terms_condition = fields.Html(
        string='Terms and Condition',
        help='Add your terms and conditions here.')
