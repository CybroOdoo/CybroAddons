from odoo import fields, models


class ResConfigSettings(models.TransientModel):
    """
        Inherits 'res.config.settings' to add field to bring approval for sale.
    """
    _inherit = 'res.config.settings'

    sale_document_approve = fields.Boolean(
        string="Sale Document Approve", config_parameter=
        'sale.sale_document_approve', help="Enable to bring approval in "
                                           "sale orders")
