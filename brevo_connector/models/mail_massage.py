from odoo import fields, models


class MailMessage(models.Model):
    """ Inherit mail_message model for adding status and to fields."""
    _inherit = 'mail.message'

    status = fields.Char(string="Status of the mail",
                         help="To identifying status of the mail")
    receiver = fields.Char(string="Receiver Name", help="Get receiver name")
