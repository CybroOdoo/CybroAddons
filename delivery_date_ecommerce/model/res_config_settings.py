from odoo import fields, models


class ResConfigSettings(models.TransientModel):
    """Inherit the model res.config.settings to add Show delivery date field"""
    _inherit = 'res.config.settings'

    is_delivery = fields.Boolean(string='Show delivery date',
                                 config_parameter='delivery_date_ecommerce.is_delivery',
                                 default=False,
                                 help="Enable, to show expected delivery time")
